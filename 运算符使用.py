# 算数(数学)运算符
print("1 + 1 = ", 1 + 1)
print("2 - 1 = ", 2 - 1)
print("3 * 3 = ", 3 * 3)
print("4 / 2 = ", 4 / 2)
# 除法取整运算
print("11 // 2 = ", 11 // 2)
# 除法取余运算
print("9 % 2 = ", 9 % 2)
# 指数运算a**b表示a的b次方
print("2 ** 2 = ", 2 ** 2)
# 赋值运算符
num = 1 + 2 + 3
print("num = ", num)
# 复合赋值运算符
num += 2
print("num += 2 :", num)
num -= 1
print("num -= 1 :", num)
num %= 2
print("num %= 2 : ", num)
num **= 2
print("num **= 2 : ", num)
num //= 2
print("num //= 2 : ", num)